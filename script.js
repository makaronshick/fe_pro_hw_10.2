/* 
Маєте масив чисел. 
Використовуйте вже існуючі методи масиву для створення нового масиву, в якому лише парні числа з оригінального масиву.
~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~
*/

const array = [2, 55, 14, 33, 89, 11, 44, 7, 31, 22];

const newArray = array.filter(element => {
  if (element % 2 === 0) {
    return element;
  }
});

